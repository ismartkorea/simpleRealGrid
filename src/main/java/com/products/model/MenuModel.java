package com.products.model;

public class MenuModel {

	private String treeNode;
	private String treeNodeParent;
	private String menuName;
	private String auth;
	private String menuLevel;
	private String orderNo;
	private String state;
	
	public String getTreeNode() {
		return treeNode;
	}
	public void setTreeNode(String treeNode) {
		this.treeNode = treeNode;
	}
	public String getTreeNodeParent() {
		return treeNodeParent;
	}
	public void setTreeNodeParent(String treeNodeParent) {
		this.treeNodeParent = treeNodeParent;
	}
	public String getMenuName() {
		return menuName;
	}
	public void setMenuName(String menuName) {
		this.menuName = menuName;
	}
	public String getAuth() {
		return auth;
	}
	public void setAuth(String auth) {
		this.auth = auth;
	}
	public String getMenuLevel() {
		return menuLevel;
	}
	public void setMenuLevel(String menuLevel) {
		this.menuLevel = menuLevel;
	}
	public String getOrderNo() {
		return orderNo;
	}
	public void setOrderNo(String orderNo) {
		this.orderNo = orderNo;
	}
	public String getState() {
		return state;
	}
	public void setState(String state) {
		this.state = state;
	}	
	
}
