package com.products.model;

public class ProcessModel {
	private String process1;
	private String processParent;
	private String task1;
	private String job1;
	private String etc;
	private String state;
	
	public String getProcess1() {
		return process1;
	}
	public void setProcess1(String process1) {
		this.process1 = process1;
	}
	public String getProcessParent() {
		return processParent;
	}
	public void setProcessParent(String processParent) {
		this.processParent = processParent;
	}
	public String getTask1() {
		return task1;
	}
	public void setTask1(String task1) {
		this.task1 = task1;
	}
	public String getJob1() {
		return job1;
	}
	public void setJob1(String job1) {
		this.job1 = job1;
	}
	public String getEtc() {
		return etc;
	}
	public void setEtc(String etc) {
		this.etc = etc;
	}
	public String getState() {
		return state;
	}
	public void setState(String state) {
		this.state = state;
	}
	

}
